##################### Operadores de Comparação #####################
#Retorna valor booleano
# > (maior que)
# < (menor que)
# == (igual a)
# != (diferente de)
# >= (maior ou igual a)
# <= (menor ou igual a)

saldo = 450
saque = 200
print(saldo > saque)
print(saldo < saque)
print(saldo == saque)
print(saldo != saque)
print(saldo >= saque)
print(saldo <= saque)